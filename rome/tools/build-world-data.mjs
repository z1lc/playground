// Generates ../world-data.js from Natural Earth GeoJSON.
// Run: node build-world-data.mjs

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { geoConicConformal, geoPath } from 'd3-geo';
import bboxClip from '@turf/bbox-clip';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const RAW = path.join(__dirname, 'raw');
const OUT = path.join(__dirname, '..', 'world-data.js');

const BBOX = [-12, 22, 50, 60];   // [west, south, east, north]
const WIDTH = 900, HEIGHT = 500;
const MARGIN = 10;
const PROJ_PARAMS = { lon0: 20, lat0: 40, lat1: 30, lat2: 50 };

function clipFeatures(fc) {
  // turf bboxClip returns a Feature with possibly empty geometry; filter.
  const out = [];
  for (const f of fc.features) {
    const clipped = bboxClip(f, BBOX);
    if (!clipped.geometry) continue;
    const c = clipped.geometry.coordinates;
    // Drop empty geometries
    if (!c || (Array.isArray(c) && c.length === 0)) continue;
    out.push(clipped);
  }
  return { type: 'FeatureCollection', features: out };
}

function main() {
  const land = JSON.parse(fs.readFileSync(path.join(RAW, 'ne_50m_land.json'), 'utf8'));
  const lakes = JSON.parse(fs.readFileSync(path.join(RAW, 'ne_50m_lakes.json'), 'utf8'));
  console.log(`Loaded land=${land.features.length} lakes=${lakes.features.length} features`);

  // ne_50m_land already excludes Caspian Sea; we don't need a separate lakes overlay.
  // (Caspian shows as sea-colored automatically since it's not in land features.)
  const landClipped = clipFeatures(land);
  const lakesClipped = { type: 'FeatureCollection', features: [] };
  console.log(`After clip: land=${landClipped.features.length}`);

  // Projection: Lambert Conformal Conic
  // d3.geoConicConformal uses rotate([-lon0, 0]) + parallels + center([0, lat0])
  const projection = geoConicConformal()
    .parallels([PROJ_PARAMS.lat1, PROJ_PARAMS.lat2])
    .rotate([-PROJ_PARAMS.lon0, 0])
    .center([0, PROJ_PARAMS.lat0]);

  projection.fitExtent(
    [[MARGIN, MARGIN], [WIDTH - MARGIN, HEIGHT - MARGIN]],
    landClipped
  );

  const scale = projection.scale();
  const [tx, ty] = projection.translate();
  console.log(`Projection: scale=${scale.toFixed(2)} translate=[${tx.toFixed(2)}, ${ty.toFixed(2)}]`);

  const pathGen = geoPath(projection);

  // Round path coordinates to 2 decimal places
  function roundD(d) {
    return d ? d.replace(/-?\d+\.\d{3,}/g, m => Number(m).toFixed(2)) : null;
  }

  const landPaths = landClipped.features.map(f => roundD(pathGen(f))).filter(Boolean);
  const lakePaths = lakesClipped.features.map(f => roundD(pathGen(f))).filter(Boolean);

  // Verify projection: project Rome (12.5°E, 41.9°N) and log result
  const romePixel = projection([12.4964, 41.9028]);
  console.log(`Rome at SVG: [${romePixel[0].toFixed(2)}, ${romePixel[1].toFixed(2)}]`);

  // Sanity check: that should land in the projected interior. Width=900, height=500.
  if (romePixel[0] < 100 || romePixel[0] > 800 || romePixel[1] < 50 || romePixel[1] > 450) {
    console.warn('WARNING: Rome projection looks suspicious');
  }

  const out = `// Generated by tools/build-world-data.mjs — do not edit by hand.
window.__WORLD__ = {
  projection: {
    lon0: ${PROJ_PARAMS.lon0}, lat0: ${PROJ_PARAMS.lat0},
    lat1: ${PROJ_PARAMS.lat1}, lat2: ${PROJ_PARAMS.lat2},
    scale: ${scale.toFixed(4)},
    tx: ${tx.toFixed(4)}, ty: ${ty.toFixed(4)},
    width: ${WIDTH}, height: ${HEIGHT}
  },
  bbox: [${BBOX.join(', ')}],
  landPaths: ${JSON.stringify(landPaths)},
  lakePaths: ${JSON.stringify(lakePaths)}
};
`;
  fs.writeFileSync(OUT, out);
  console.log(`Wrote ${OUT}: ${out.length} bytes (${landPaths.length} land paths, ${lakePaths.length} lake paths)`);
}

main();
